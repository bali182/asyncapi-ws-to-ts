import { OpenAPIGeneratorContext, TsUnit } from '../../generatorTypes'
import { Type } from '../../types/types'
import { generateTypeAst } from './astCreators'
import { getTypeImports } from './typeImports'

export function generateType(type: Type, context: OpenAPIGeneratorContext): TsUnit {
  return {
    content: [generateTypeAst(context.config.name(type), type)],
    imports: getTypeImports(type, context),
    path: context.config.path(type, context.config.name),
  }
}
